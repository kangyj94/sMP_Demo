package kr.co.bitcube.system.dto;

public class CodeTypesDto {

	private String codeTypeId;
	private String codeTypeCd;
	private String codeTypeNm;
	private String codeTypeDesc;
	private String codeFlag;
	private String isUse;
	
	public String getCodeTypeId() {
		return codeTypeId;
	}
	public void setCodeTypeId(String codeTypeId) {
		this.codeTypeId = codeTypeId;
	}
	public String getCodeTypeCd() {
		return codeTypeCd;
	}
	public void setCodeTypeCd(String codeTypeCd) {
		this.codeTypeCd = codeTypeCd;
	}
	public String getCodeTypeNm() {
		return codeTypeNm;
	}
	public void setCodeTypeNm(String codeTypeNm) {
		this.codeTypeNm = codeTypeNm;
	}
	public String getCodeTypeDesc() {
		return codeTypeDesc;
	}
	public void setCodeTypeDesc(String codeTypeDesc) {
		this.codeTypeDesc = codeTypeDesc;
	}
	public String getCodeFlag() {
		return codeFlag;
	}
	public void setCodeFlag(String codeFlag) {
		this.codeFlag = codeFlag;
	}
	public String getIsUse() {
		return isUse;
	}
	public void setIsUse(String isUse) {
		this.isUse = isUse;
	}
}
